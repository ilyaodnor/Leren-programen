def рассчитать_ингредиенты(количество_человек):
    # Ингредиенты для 4 человек
    ингредиенты = {
        'мука (граммы)': 200,
        'молоко (мл)': 300,
        'яйца': 2,
        'сахар (столовые ложки)': 1,
        'соль (щепотки)': 1
    }

    # Пропорция на основе 4 человек
    пропорция = количество_человек / 4

    # Расчет ингредиентов для заданного количества человек
    for ингредиент, количество in ингредиенты.items():
        ингредиенты[ингредиент] = round(количество * пропорция, 1)

    return ингредиенты

def показать_ингредиенты(ингредиенты):
    print("\nНеобходимые ингредиенты:")
    for ингредиент, количество in ингредиенты.items():
        print(f"- {ингредиент}: {количество}")

def main():
    try:
        количество_человек = float(input("Введите количество человек: "))
        if количество_человек <= 0:
            print("Количество человек должно быть больше 0.")
            return

        ингредиенты = рассчитать_ингредиенты(количество_человек)
        показать_ингредиенты(ингредиенты)

    except ValueError:
        print("Введите корректное количество человек (число).")

if __name__ == "__main__":
    main()
